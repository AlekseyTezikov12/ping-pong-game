let shuffledCongrats = [];
let currentIndex = 0;

function shuffleArray(arr) {
    return arr.slice().sort(() => 0.5 - Math.random());
}

const btn = document.getElementById('generate-btn');
const list = document.getElementById('congrats-list');

btn.addEventListener('click', function() {
    if (shuffledCongrats.length === 0) {
        shuffledCongrats = shuffleArray(congratulations);
        currentIndex = 0;
        list.innerHTML = '';
    }
    if (currentIndex < shuffledCongrats.length) {
        const li = document.createElement('li');
        li.textContent = shuffledCongrats[currentIndex];
        list.appendChild(li);
        currentIndex++;
        if (currentIndex === 1) {
            btn.textContent = 'Нажми ещё';
        }
        if (currentIndex === shuffledCongrats.length) {
            btn.textContent = 'Поздравления закончились!';
            btn.disabled = true;
        }
    }
}); 